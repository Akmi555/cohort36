public class Task1Suffixes {

  // суффиксы для чисел
  public static void main(String[] args) {
    // шаг;
    // Литерал (англ. literal), или безымянная константа (англ. nameless constant) —
    // запись в исходном коде (source code), представляющая собой фиксированное значение.
    System.out.println("Привет"); // "Привет" - литерал (строка)
    int x = 123; // 123 - литерал (число)
    // Литералы:
    // 'а' - символ (char)
    System.out.println('а');
    // "Привет" - строка (String)
    System.out.println("Привет");
    // 123 - целое число (int)
    System.out.println(123);
    // 123L - целое число 123, но long - суффикс L обозначает другой тип литерала
    System.out.println(123L); // при выводе суффикс не видно, выводится только число
    // 2.5 - дробное число (double)
    System.out.println(2.5);
    // 2.5f - дробное число 2.5, но float - суффикс f обозначает другой тип литерала
    System.out.println(2.5f); // при выводе суффикс не видно, выводится только число
    System.out.println(2f); // float со значением 2.0
  }
}
